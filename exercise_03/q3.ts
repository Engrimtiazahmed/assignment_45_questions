//store a person name in variable , and then print person name in lowercase ,uppercase and titlecase


let person_name :string = "Imtiaz Ahmed";
console.log(person_name.toLowerCase()); //lower_case

console.log(person_name.toUpperCase()); //Upper_case

console.log(person_name.charAt(0).toUpperCase()+person_name.slice(1)); //title_case